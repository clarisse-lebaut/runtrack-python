def retournement(mot):
        retourner_le_mot=""
        for letter in mot:
                retourner_le_mot = letter + retourner_le_mot
        print(retourner_le_mot)

retournement("toni poka")
retournement("akop inot")
retournement("macao")
retournement("oacam")
retournement("anakin")
retournement("nikana")
